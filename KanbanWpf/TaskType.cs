
namespace KanbanWpf
{
    /// <summary>
    /// Specifies the available task types.
    /// </summary>
    public enum TaskType
    {
        /// <summary>
        /// The task is in "ToDo" state.
        /// </summary>
        Todo,
        /// <summary>
        /// The task is in "In progress" state.
        /// </summary>
        InProgress,
        /// <summary>
        /// The task is in "Done" state.
        /// </summary>
        Done,
    }
}
